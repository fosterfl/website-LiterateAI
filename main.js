function addText(value) {
  var bodyText = document.getElementById("text-container")

  Object.keys(copyText[value]).forEach(function(id){

    if (!copyText[value][id]["text"] || document.getElementById(id).textContent !== copyText[value][id]["text"] || copyText[value][id]["text"] === "") {
      document.getElementById(id).classList.remove("active")
      document.getElementById(id).classList.add("hidden")
      setTimeout(function () {
        document.getElementById(id).innerHTML = copyText[value][id]["text"]
        document.getElementById(id).className = "active"
        if (copyText[value][id]["class"] != "") {
          document.getElementById(id).classList.add(copyText[value][id]["class"])
        } else {
        //  document.getElementById(id).className = ""
        }

        annotate()
      }, fadeTimer)
    }
  })

  document.getElementById("sentimentSelector").value = value

}

